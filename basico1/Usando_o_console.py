# Aqui vou ensinar como usar o print, e claro, a como concatenar

nome = "Ada"
idade = 36

print("Olá, meu nome é Ada e eu tenho 36 anos.")
# O print serve para você "printar" o que estiver dentro dos parênteses,
# tendo em mente que, tudo o que estiver entre "" ou '' são strings.
# Caso não esteja, o programa vai entender que são variáveis e procurará por elas.

print("Olá, meu nome é",nome,"e eu tenho",idade,"anos.")
# Aqui, eu fiz uma concatenação, ou seja, eu comcateinei textos do próprio print, com variáveis
# Veja que entre textos que tem "" e palavras, há uma vírgula, o programa entede que ali tem uma
# concatenação, ou seja, o que vier em seguida.
# Para iniciar o programa, clique com o botão direito na área do código ou no nome dele no topo da IDE
# e clique em "Run".