# Primeiro, vamos entender o básico da lógica de pogramação em Python.
# Assim como em outras linguagens, existem variáveis e tipos.

# Variáveis são como pequenas caixinhas que guardam um valor que pode ser do mais variável tipo.
# Os tipos são o que é aquele valor, e como o programa deve interpretá-lo.
# Os principais tipos de valores são: string, int, bool e float

nome_one = "Ada Lovelace"
# "nome_one" é uma variável, e seu valor armazenado é "Ada Lovelace".
# Isto significa que esta variável é uma string.
# Strings são valores entre " " ou ' '. Estes valores são lidos pelo computador como textos,
# pois dentro dele podem conter: A-Z / a-z / números / !@#$%¨&*()

idade_one = 36
# "idade_one" também é uma variável. Esta por sua vez, é uma variável do tipo inteiro (int).
# Variáveis do tipo inteiro são variáveis que guardam apenas valores números postivos e n
# egativos que sejam inteiros, como: -12, 0, 18, -2134, 990921
# Valores quebrados não são valores inteiros, lembre-se disso! :)

altura_one = 1.67
# por fim, a variável "altura_one" tem um valor quebrado, um valor do grupo de números reais,
# ou seja, float. Valores números quebrados são normalmente escritos com vírgulas, contudo,
# na programação é usado um ponto. Estes valores támbem podem ser negativos.
# Lembre-se disto: Todo número inteiro é um número real, mas nem todo número real é um número inteiro!

# Por fim, temos os valores booleanos, também conhecidos como tipo bool.
# Os valores booleanos são valores verdadeiro ou falso, e são muito usados para por exemplo,
# Verificar condições, ativar interruptores e muito mais!
programmer = True
# Aqui diz que programmer é verdadeiro
enginner = False
# Já aqui, é falso.